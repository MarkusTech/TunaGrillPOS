        jTextDisplay.setText(jTextDisplay.getText() + "\t\t\t\t\t" + "Balance: " + bal + "\n");
        jTextDisplay.setText(jTextDisplay.getText() + "\n");
        jTextDisplay.setText(jTextDisplay.getText() + "******************************************************\n");
        //footer
        jTextDisplay.setText(jTextDisplay.getText() + "*                  Wenn Mark Recopelacion            *\n");

