    private void jTextProductCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextProductCodeKeyPressed
             //key press

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String pcode = jTextProductCode.getText();

            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/pos", "root", "w3nnm4rk09125");
                stmt = con.prepareStatement("Select * from product where id = ?");

                stmt.setString(1, pcode);
                rs = stmt.executeQuery();

                if (rs.next() == false) {

                    JOptionPane.showMessageDialog(this, "Product Code Not Found");
                } else {
                    String pname = rs.getString("product_name");
                    String price = rs.getString("Price");

                    jTextProductName.setText(pname.trim());
                    jTextPrice.setText(price.trim());
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(POS.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(POS.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jTextProductCodeKeyPressed

